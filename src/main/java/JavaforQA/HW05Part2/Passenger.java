package JavaforQA.HW05Part2;

public class Passenger {

        private String name;
        private boolean firstClass;

        public Passenger(String name, boolean firstClass) {
            this.name = name;
            this.firstClass = firstClass;
        }

        public String getName() {
            return name;
        }

        public boolean isFirstClass() {
            return firstClass;
        }
    }




